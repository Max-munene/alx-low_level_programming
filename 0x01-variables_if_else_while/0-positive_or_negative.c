#include <stdlib.h>
#include <time.h>
#include<stdio.h>
/**
 * main - prints n positive, negative or zero
 *
 * Description: checks and prints outcomes of n as positive
 * or negative or zero
 *
 * Return: 0 (success)
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	if (n > 0)
	{
		printf("%i is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%i is zero\n", n);
	}
	else if (n < 0)
	{
		printf("%i is negative\n", n);
	}
	return (0);
}


